# ref: https://tiefenauer.github.io/blog/smith-waterman/

import itertools
import numpy as np


def matrix(a, b, match_score=3., gap_cost=2.):
    """
    >>> a = 'ggttgacta'
    >>> len(a)
    9
    >>> b = 'tgttacgg'
    >>> len(b)
    8
    >>> H = matrix(a, b)
    >>> H.shape
    (10, 9)
    """
    H = np.zeros((len(a) + 1, len(b) + 1))
    for i, j in itertools.product(range(1, H.shape[0]), range(1, H.shape[1])):
        match = H[i - 1, j - 1] + (match_score if a[i - 1] == b[j - 1] else -match_score)
        delete = H[i - 1, j] - gap_cost
        insert = H[i, j - 1] - gap_cost
        H[i, j] = max(match, delete, insert, 0)
    return H


def traceback(H, b, b_='', old_i=0, score=0):
    """
    >>> a = 'ggttgacta'
    >>> b = 'tgttacgg'
    >>> H = matrix(a, b)
    >>> traceback(H, b)
    ('gtt-ac', 1, 41.0)
    """
    # flip H to get index of **last** occurrence of H.max() with np.argmax()
    H_flip = np.flip(np.flip(H, 0), 1)
    i_, j_ = np.unravel_index(H_flip.argmax(), H_flip.shape)
    i, j = np.subtract(H.shape, (i_ + 1, j_ + 1))  # (i, j) are **last** indexes of H.max()
    score += H[i, j]
    if H[i, j] == 0:
        return b_, j, score
    b_ = b[j - 1] + '-' + b_ if old_i - i > 1 else b[j - 1] + b_
    return traceback(H=H[0:i, 0:j], b=b, b_=b_, old_i=i, score=score)


def smith_waterman(a, b, match_score=3, gap_cost=2):
    """
    >>> a = 'ggttgacta'
    >>> b = 'tgttacgg'
    >>> smith_waterman(a, b)
    (1, 7, 41.0)
    """
    a, b = a.upper(), b.upper()
    H = matrix(a, b, match_score, gap_cost)
    b_, pos, score = traceback(H, b)
    return pos, pos + len(b_), score


