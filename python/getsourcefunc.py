# Returns the source code of a given python function
# See: https://stackoverflow.com/a/427533/1679629

import inspect
import os
import importlib
from io import StringIO
from contextlib import redirect_stdout


def print_source(modulefilename, functionname):
    modulename = os.path.splitext(modulefilename)[0]
    f = StringIO()
    with redirect_stdout(f):
        exec(f'from {modulename} import {functionname} as mod; lines=inspect.getsource(mod); print(lines)')
    lines = f.getvalue()
    return lines


def print_attributes(modulefilename):
    modulename = os.path.splitext(modulefilename)[0]
    mod = importlib.import_module(modulename)
    attr_list = dir(mod)
    for attr in attr_list:
        print(attr)


