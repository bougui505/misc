import os

import numpy as np


def GetScriptDir():
    scriptpath = os.path.realpath(__file__)
    scriptdir = os.path.dirname(scriptpath)
    return scriptdir


def resample(data, ratio):
    n = data.shape[0]
    size = int(n * ratio)
    inds = np.arange(n)
    data = np.c_[inds, data]
    sel = np.random.choice(a=n, size=size, replace=False)
    data = data[sel]
    data = data[data[:, 0].argsort()]
    return data


if __name__ == "__main__":
    import argparse
    import doctest
    import sys

    # argparse.ArgumentParser(prog=None, usage=None, description=None, epilog=None, parents=[], formatter_class=argparse.HelpFormatter, prefix_chars='-', fromfile_prefix_chars=None, argument_default=None, conflict_handler='error', add_help=True, allow_abbrev=True, exit_on_error=True)
    parser = argparse.ArgumentParser(description="Resample stdin")
    # parser.add_argument(name or flags...[, action][, nargs][, const][, default][, type][, choices][, required][, help][, metavar][, dest])
    parser.add_argument(
        "-r", "--ratio", type=float, help="Resampling ration between 0.0 and 1.0"
    )
    parser.add_argument(
        "-i",
        "--ind",
        help="Print selected indices in the first row of input. Add a row compared with the original data.",
        action="store_true",
    )
    parser.add_argument("--test", help="Test the code", action="store_true")
    parser.add_argument("--func", help="Test only the given function(s)", nargs="+")
    args = parser.parse_args()

    if args.test:
        if args.func is None:
            doctest.testmod(
                optionflags=doctest.ELLIPSIS | doctest.REPORT_ONLY_FIRST_FAILURE
            )
        else:
            for f in args.func:
                print(f"Testing {f}")
                f = getattr(sys.modules[__name__], f)
                doctest.run_docstring_examples(
                    f,
                    globals(),
                    optionflags=doctest.ELLIPSIS | doctest.REPORT_ONLY_FIRST_FAILURE,
                )
        sys.exit()

    data = np.genfromtxt(sys.stdin, dtype=float)
    if args.ratio is None:
        sys.exit("Provide the --ratio (-r) argument")
    data = resample(data, args.ratio)
    data = data[:, 1:] if not args.ind else data
    np.savetxt(sys.stdout, data, fmt="%s")
